const jobRole = "Front-End Developer";

switch(jobRole) {
    case "Front-End Developer" :
      console.log("You are a Front End Developer.");
      break;
    case "Back-End Developer" :
      console.log("You are a Back End Developer.");
      break;
    case "Full Stack Developer" :
      console.log("You are a Full Stack Developer.");
      break;
    default:
        console.log("Please state your job role.")
}
